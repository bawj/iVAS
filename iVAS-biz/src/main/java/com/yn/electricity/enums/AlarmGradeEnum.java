package com.yn.electricity.enums;

/**
 * @author 报警等级
 */
public enum AlarmGradeEnum {

    /***/
    ALARM_GRADE_0(0, "一般"),
    ALARM_GRADE_1(1, "重要"),
    ALARM_GRADE_2(2, "紧急"),
    ALARM_GRADE_3(3, "非常紧急"),

    ;

    private final Integer code;

    private final String msg;


    AlarmGradeEnum(Integer code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public Integer getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }

}
