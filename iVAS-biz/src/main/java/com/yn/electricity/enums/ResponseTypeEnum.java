package com.yn.electricity.enums;

/**
 * @author admin
 * Date 2021/5/28 14:32
 * Description 响应类型
 **/
public enum ResponseTypeEnum {

    /**
     * 响应类型
     */
    RELAY_OUTPUT(1, "控制继电器输入"),
    PLAY_THE_TONE(2, "播放提示音"),
    CAPTURE(3, "抓图"),
    VIDEOTAPE(4, "报警录像"),
    ;

    private Integer status;
    private String msg;

    ResponseTypeEnum(Integer status, String msg) {
        this.status = status;
        this.msg = msg;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

}
